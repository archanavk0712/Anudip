package day2;

public class Switch
{
	public static void main(String[] args) {
		int option=9;
		switch(option)
		{
		case 1: System.out.println("option choosed is 1");
		break;
		case 2: System.out.println("option choosed is 2");
		break;
		case 3: System.out.println("option choosed is 3");
		break;
		case 4: System.out.println("option choosed is 4");
		break;
		case 5: System.out.println("option choosed is 5");
		break;
		case 6: System.out.println("option choosed is 6");
		break;
		default: System.out.println("Invalid option");
		break;
		}
	}

}
